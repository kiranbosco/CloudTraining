package com.collections;

public class DataStructor {
	
	public static void main(String[] args) {
		
		
		String s = "12354212";
		String[] sss =s.split("\\3");
		
		for(String s1 : sss) {
		System.out.println(s1);
				
		}
		
		String splt ="12121212121";
		String[] spt =splt.split("2");
		for(String ss : spt) {
			System.out.println(ss);
			System.out.println();
		
	}

}
}
