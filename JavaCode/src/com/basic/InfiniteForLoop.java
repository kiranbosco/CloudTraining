package com.basic;

public class InfiniteForLoop {

	public static void main(String[] args) {
		
		
		int j =1;
		while(j <=5) {
			System.out.println(j);
			j++;
		}
		
		

		for(;;) {
		
			System.out.println("this is the infinite for loop");
		}
		
		// while loop exe it can be using to iterate the block of code

		    
	}
}
